static void get_pinyin(const char * str, std::string & pinyin)
{
	pinyin = "";
	const unsigned char * chr = (const unsigned char *)str;	
	int r, c;
	while (*chr)
	{
		if (*chr > 0xA0 && *chr < 0xFF)					//汉字
		{
			r = *chr++ - 0xA1;
			c = *chr++ - 0xA1;
			if (c >= 0 && c < 94)
				pinyin.append(1, PINYIN_GB[r * 94 + c]);
		}
		else if (*chr > 32 && *chr < 127)
		{
			if (*chr >= 'a' && *chr < 'z')
				pinyin.append(1, *chr++ + 'A' - 'a');	//字母变大写
			else
				pinyin.append(1, *chr++);				//数字及符号照原样返回
		}
		else
			chr++;
	}
}