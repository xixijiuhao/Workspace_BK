static void get_pinyin(const wchar_t * wstr, std::string & pinyin)
{
	pinyin = "";
	const unsigned short * wchr = (const unsigned short *)wstr;
	while (*wchr)
	{
		if (*wchr >= 0x4e00 && *wchr <= 0x9fa5)
			pinyin.append(1, PINYIN_UTF16[*wchr++ - 0x4e00]);
		else if (*wchr >= 32 && *wchr <= 127)
		{	
			if (*wchr >= L'a' && *wchr <= L'z')
				pinyin.append(1, (char)(*wchr++ - 32));	//字母变大写
			else
				pinyin.append(1, (char)*wchr++);	
		}
		else 
			wchr++;
	}
}

